def sum_range(start, end):
    # Инициализируем переменную result с нулем для хранения суммы
    result = 0
    
    # Проходим по всем числам от start до end включительно
    for i in range(start, end + 1):
        # Прибавляем текущее число i к result
        result = result + i
        # Выводим текущую промежуточную сумму на экран
        print(result)
    
    # Возвращаем итоговую сумму после завершения цикла
    return result

# Вызываем функцию sum_range с аргументами 2 и 6
# и сохраняем возвращаемое значение в переменную sum
sum = sum_range(2, 6)

# Выводим конечный результат на экран
print(f"Конечный результат: {sum}")