# Объявляем переменные
имя = "Алиса"
возраст = 25
город = "Москва"

# Используем переменные
print(f"Привет, {имя}!")
print(f"Тебе {возраст} лет.")
print(f"Ты живешь в городе {город}.")

# Изменяем значение переменной
возраст += 1
print(f"Скоро тебе будет {возраст} лет.")


#В этом примере мы создаем три переменные: `имя`, `возраст` и `город`,
#затем используем их для вывода информации на экран. 
#После этого изменяем значение переменной `возраст` и снова выводим информацию.