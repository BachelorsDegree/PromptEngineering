import math
#math.ceil() - округляет в большую сторону
print(math.ceil(2.3))
#math.floor() - округляет в меньшую сторону
print(math.floor(2.9))
#math.log() - возвращает натуральный логарифм
print(math.log(100, 10))
#math.pow() - возведение в степень
print(math.pow(2, 3))
#math.sqrt() - возвращает квадратный корень
print(math.sqrt(9))
#math.pi - возвращает число Пи
print(math.pi)