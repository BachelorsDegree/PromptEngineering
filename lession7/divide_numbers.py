def divide_numbers():
    try:
        # Запрашиваем у пользователя ввод двух чисел
        num1 = float(input("Введите первое число: "))
        num2 = float(input("Введите второе число: "))
        
        # Пытаемся выполнить деление
        result = num1 / num2
        
    except ZeroDivisionError:
        # Обработка ошибки деления на ноль
        print("Ошибка: Деление на ноль невозможно.")
        
    except ValueError:
        # Обработка ошибки, если введено не число
        print("Ошибка: Пожалуйста, введите корректное число.")
        
    else:
        # Если ошибки нет, выводим результат деления
        print(f"Результат деления: {result}")

# Вызов функции
divide_numbers()

### Объяснение:
# **`try:`** — Начало блока, где помещается код, который может вызвать ошибку.
# **`except ZeroDivisionError:`** — Этот блок выполняется, 
# если возникает ошибка деления на ноль (`ZeroDivisionError`).
# **`except ValueError:`** — Этот блок выполняется, 
# если пользователь вводит не числовое значение, 
# и возникает ошибка преобразования (`ValueError`).
# **`else:`** — Этот блок выполняется, если в блоке `try` не возникло исключений, 
# и деление прошло успешно.