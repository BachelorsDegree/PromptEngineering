def sum_of_list(numbers):
    # Инициализация переменной для хранения суммы
    total = 0
    # Проход по каждому числу в списке
    for number in numbers:
        # Добавление текущего числа к общей сумме
        total += number
    # Возврат итоговой суммы
    return total

def main():
    # Инициализация пустого списка для хранения чисел
    numbers = []
    
    print("Введите до 10 чисел для суммирования. Введите 'q' для завершения ввода раньше.")

    # Цикл с фиксированным количеством итераций
    for _ in range(10):
        # Запрашиваем ввод числа у пользователя
        user_input = input("Введите число: ")
        
        # Проверяем, если пользователь хочет завершить ввод
        if user_input.lower() == 'q':
            break
        
        try:
            # Преобразуем введённое значение в число и добавляем в список
            number = float(user_input)
            numbers.append(number)
        except ValueError:
            # Обработка некорректного ввода
            print("Пожалуйста, введите корректное число.")
    
    # Вычисление суммы всех чисел в списке
    result = sum_of_list(numbers)
    # Вывод результата
    print(f"Сумма всех чисел в списке: {result}")

# Запуск основной функции
if __name__ == "__main__":
    main()