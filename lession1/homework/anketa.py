def main():
  print("Добро пожаловать в анкету!")

  # Создаем словарь для хранения данных
  анкета = {}

  # Запрашиваем имя
  имя = input("Как вас зовут? ")
  анкета['Имя'] = имя

  # Запрашиваем возраст
  возраст = input("Сколько вам лет? ")
  анкета['Возраст'] = возраст

  # Запрашиваем любимый цвет
  цвет = input("Какой у вас любимый цвет? ")
  анкета['Любимый цвет'] = цвет

  # Выводим результаты
  print("\nСпасибо за участие в анкете!")
  print("Вот ваши ответы:")
  for ключ, значение in анкета.items():
      print(f"{ключ}: {значение}")

if __name__ == "__main__":
  main()